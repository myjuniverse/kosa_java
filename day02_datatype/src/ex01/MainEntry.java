package ex01;

public class MainEntry {
	public static void main(String[] args) {
		
		System.out.print("MSA\n th2\t 화이팅!\n\n"); // \문자 : 제어문자, \n, \t, \a, \b
		System.out.println(100); // 4byte int
		System.out.println(100L); // 8byte long l/L
		System.out.print(12.34); // 8byte double
		System.out.print(12.34F); // 4byte floar f/F
		System.out.println();
		System.out.printf("%s", "Jun"); // sysout Ctrl+Space
		
	}
}
